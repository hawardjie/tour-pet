// Helper function to get growth data for a specific breed
export function getBreedGrowthData(breedId: string): BreedGrowthData | undefined {
  return breedGrowthData.find((data) => data.breedId === breedId);
}

// Helper function to calculate age category
export function getAgeCategory(ageInMonths: number, breedId: string): string {
  const data = getBreedGrowthData(breedId);
  if (!data) return 'unknown';

  if (ageInMonths <= data.lifeStages.puppy.end) return 'puppy';
  if (ageInMonths <= data.lifeStages.adolescent.end) return 'adolescent';
  if (ageInMonths <= data.lifeStages.adult.end) return 'adult';
  return 'senior';
}
